#!/bin/bash
# Creates the site-packages-test directory structure in reach-testbed
# Run: bash setup-site-packages-test.sh

set -euo pipefail

BASE="$HOME/src/reach-testbed/site-packages-test"

echo "Creating site-packages test structure..."

# --- Scenario 1: .venv (standard) ---
DIR="$BASE/.venv/lib/python3.11/site-packages/fakepkg"
mkdir -p "$DIR"
cat > "$DIR/search.py" << 'PYEOF'
# Simulates pip/_internal/commands/search.py
# Contains xmlrpc usage that semgrep flags — but this is library code,
# not customer code. MUST NOT be reported as a finding.
import xmlrpc.client

def search_packages(query):
    """pip's package search via xmlrpc — flagged by semgrep as CWE."""
    pypi = xmlrpc.client.ServerProxy('https://pypi.org/pypi')
    return pypi.search({'name': query})
PYEOF

# --- Scenario 2: venv (standard) ---
DIR="$BASE/venv/lib/python3.11/site-packages/fakepkg"
mkdir -p "$DIR"
cat > "$DIR/db_utils.py" << 'PYEOF'
# Simulates a library that builds SQL internally.
# Semgrep would flag the f-string SQL. MUST NOT be reported.
import sqlite3

def get_schema_info(table_name):
    """Library-internal SQL — not user-controlled."""
    conn = sqlite3.connect(':memory:')
    conn.execute(f"SELECT * FROM sqlite_master WHERE name = '{table_name}'")
    conn.close()
PYEOF

# --- Scenario 3: myenv (non-standard — the customer's actual case) ---
DIR="$BASE/myenv/lib/python3.11/site-packages/fakepkg"
mkdir -p "$DIR"
cat > "$DIR/legacy_rpc.py" << 'PYEOF'
# Simulates a third-party library using xmlrpc and eval.
# Non-standard venv name — previously NOT excluded. MUST NOT be reported.
import xmlrpc.client
import os

def parse_response(data):
    """Library code with eval — NOT customer code."""
    return eval(data)

def run_command(cmd):
    """Library code with os.system — NOT customer code."""
    os.system(cmd)
PYEOF

# --- Scenario 4: conda_env (conda-style — also non-standard) ---
DIR="$BASE/conda_env/lib/python3.11/site-packages/fakepkg"
mkdir -p "$DIR"
cat > "$DIR/unsafe_utils.py" << 'PYEOF'
# Simulates a conda-installed library with dangerous patterns.
# MUST NOT be reported — it's third-party library code.
import subprocess
import pickle

def execute(command):
    """Library helper — subprocess with shell=True."""
    return subprocess.check_output(command, shell=True)

def load_data(raw_bytes):
    """Library helper — pickle.loads (CWE-502)."""
    return pickle.loads(raw_bytes)
PYEOF

echo ""
echo "Created test structure:"
find "$BASE" -name "*.py" -not -name "app.py" | sort | while read f; do
    echo "  $f"
done
echo ""
echo "  $BASE/app.py (real finding — MUST be detected)"
echo ""
echo "Done. Run: reachctl scan $BASE"
